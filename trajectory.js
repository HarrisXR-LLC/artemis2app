// T0: actual liftoff (same as index.html)
const T0_MS = Date.UTC(2026, 3, 1, 22, 35, 12); // 2026-04-01T22:35:12Z

// Pre-compute MET (seconds from T0) for each OEM entry
const OEM_WITH_MET = OEM_POSITIONS.map(entry => {
  const ts = entry[0];
  // Parse "2026-04-02T01:57:37.084" as UTC
  const utcMs = Date.parse(ts + 'Z'); // append Z to force UTC
  const metSec = (utcMs - T0_MS) / 1000;
  return { met: metSec, x: entry[1], y: entry[2], z: entry[3] };
});

// MET range covered by the OEM
const OEM_MET_START = OEM_WITH_MET[0].met;
const OEM_MET_END = OEM_WITH_MET[OEM_WITH_MET.length - 1].met;

/**
 * Interpolate Orion's Earth-centered position at a given MET.
 * Returns { x, y, z } in scene units, or null if MET is outside OEM range.
 */
function getOrionPositionAtMET(metSec) {
  if (metSec < OEM_MET_START || metSec > OEM_MET_END) return null;

  // Binary search for the bracketing samples
  let lo = 0, hi = OEM_WITH_MET.length - 1;
  while (lo < hi - 1) {
    const mid = (lo + hi) >> 1;
    if (OEM_WITH_MET[mid].met <= metSec) lo = mid;
    else hi = mid;
  }

  const a = OEM_WITH_MET[lo];
  const b = OEM_WITH_MET[hi];
  const dt = b.met - a.met;
  const t = dt > 0 ? (metSec - a.met) / dt : 0;

  return {
    x: a.x + (b.x - a.x) * t,
    y: a.y + (b.y - a.y) * t,
    z: a.z + (b.z - a.z) * t
  };
}

// Mission end MET (splashdown): 9d 01:42:48 = 783768 seconds
const MISSION_END_MET = 9 * 86400 + 1 * 3600 + 42 * 60 + 48;

/**
 * Get Orion's Earth-relative position at any MET, including connector segments.
 * - Before OEM start: linear interpolation from Earth surface to first OEM point
 * - During OEM range: interpolated from OEM data
 * - After OEM end: linear interpolation from last OEM point back to Earth surface
 * Returns { x, y, z } in scene units, or null if completely outside mission.
 */
function getOrionFullPosition(metSec) {
  if (metSec < 0 || metSec > MISSION_END_MET) return null;

  // Before OEM: launch connector (Earth surface → first OEM point)
  if (metSec < OEM_MET_START) {
    const first = OEM_WITH_MET[0];
    const t = metSec / OEM_MET_START; // 0 at launch, 1 at OEM start
    // Start at near-zero (Earth surface) — use a small offset in the direction of the first OEM point
    return {
      x: first.x * t,
      y: first.y * t,
      z: first.z * t
    };
  }

  // During OEM: use the OEM interpolation
  if (metSec <= OEM_MET_END) {
    return getOrionPositionAtMET(metSec);
  }

  // After OEM: splashdown connector (last OEM point → Earth surface)
  const last = OEM_WITH_MET[OEM_WITH_MET.length - 1];
  const remaining = MISSION_END_MET - OEM_MET_END;
  const t = remaining > 0 ? (metSec - OEM_MET_END) / remaining : 1; // 0 at OEM end, 1 at splashdown
  return {
    x: last.x * (1 - t),
    y: last.y * (1 - t),
    z: last.z * (1 - t)
  };
}

